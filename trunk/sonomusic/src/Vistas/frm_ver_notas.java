    private void ver_nota(String query) {
         try {
        mostrar = new DefaultTableModel()
 {@Override
     public boolean isCellEditable (int fila, int columna) {
         return false;
     }
 };
        Statement st= con.conexion();
        ResultSet rs = con.consulta(st, query);
       
        //Establecer como cabezeras el nombre de las colimnas
        mostrar.addColumn("Id");
        mostrar.addColumn("Fecha Reg.");
        mostrar.addColumn("Fecha Eje.");
        mostrar.addColumn("Descripcion");
        mostrar.addColumn("Usuario");
        mostrar.addColumn("Estado");
        
        //Creando las filas para el JTable
        while (rs.next()) {
            Object[] fila = new Object[6];
            fila[0]=rs.getObject("idNotas");
            fila[1]=rs.getObject("fecha_reg");
            fila[2]=rs.getObject("fecha_eje");
            fila[3]=rs.getObject("glosa");
            fila[4]=rs.getObject("nick");
            String estado = rs.getString("estado");
            if (estado.equals("0")) {
                fila[5]="Pendiente";
            } else {
                fila[5]="Relizado";
            }
            

            mostrar.addRow(fila);
        }
        con.cerrar(st);
        con.cerrar(rs);
        t_notas.setModel(mostrar);
        t_notas.getColumnModel().getColumn(0).setPreferredWidth(30);
        t_notas.getColumnModel().getColumn(1).setPreferredWidth(80);
        t_notas.getColumnModel().getColumn(2).setPreferredWidth(80);
        t_notas.getColumnModel().getColumn(3).setPreferredWidth(350);
        t_notas.getColumnModel().getColumn(4).setPreferredWidth(80);
        t_notas.getColumnModel().getColumn(5).setPreferredWidth(90);
        mostrar.fireTableDataChanged();
    }
    catch (SQLException e) {
        System.out.print(e);
    }
    }
