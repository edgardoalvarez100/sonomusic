    private void btn_verActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verActionPerformed
        nota.setId(Integer.parseInt(t_notas.getValueAt(i, 0).toString()));
        try {
            Statement st = con.conexion();
            String det_nota = "select glosa from Notas where idNotas = '"+nota.getId()+"'";
            ResultSet rs = con.consulta(st, det_nota);
            if (rs.next()) {
                frm_reg_nota notas = new frm_reg_nota();
                notas.txt_nota.setText(rs.getString("glosa"));
                notas.txt_nota.setEditable(false);
                notas.btn_reg.setEnabled(false);
                ven.llamar_ventana(notas);
            }
            con.cerrar(rs);
            con.cerrar(st);
        } catch (SQLException ex) {
            System.out.print(ex);
        }
        
    }//GEN-LAST:event_btn_verActionPerformed

    private void btn_finActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_finActionPerformed
    int confirmado = JOptionPane.showConfirmDialog( null, "¿Confirma la accion?");
    nota.setFec_eje(ven.getFechaActual());
    nota.setId(Integer.parseInt(t_notas.getValueAt(i, 0).toString()));
    if (JOptionPane.OK_OPTION == confirmado) {
        try {
            Statement st = con.conexion();
            String query = "update Notas set fecha_eje = '"+nota.getFec_eje()+"', estado = '1' where idNotas = '"+nota.getId()+"'";
            con.actualiza(st, query);
            con.cerrar(st);
            String ver_notas ="select * from Notas where estado = '0' order by idNotas desc";
            ver_nota(ver_notas);
        } catch (Exception ex) {
            System.out.print(ex);
        }
    }
    }//GEN-LAST:event_btn_finActionPerformed

