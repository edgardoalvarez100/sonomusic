    private void txt_con_antKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_con_antKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_con_ant.getText().length() > 0) {
                usu.setNick(txt_nick.getText());
                usu.setContra(txt_con_ant.getText());
                try {
                    Statement st = con.conexion();
                    String ver_contra = "select dni from usuario where nick = '" + usu.getNick() + "' and contra = '" + usu.getContra() + "'";
                    ResultSet rs = con.consulta(st, ver_contra);
                    if (rs.next()) {
                        txt_con_nue.setEditable(true);
                        txt_con_nue.requestFocus();
                    } else {
                        txt_con_ant.setText("");
                        txt_con_ant.requestFocus();
                    }
                    con.cerrar(rs);
                    con.cerrar(st);
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
        }
    }//GEN-LAST:event_txt_con_antKeyPressed

    private void txt_con_nueKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_con_nueKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_con_nue.getText().length() > 0) {
                txt_con_nue2.setEditable(true);
                txt_con_nue2.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_con_nueKeyPressed

    private void txt_con_nue2KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_con_nue2KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txt_con_nue2.getText().equals(txt_con_nue.getText())) {
                btn_graba.setEnabled(true);
                btn_graba.requestFocus();
            } else {
                txt_con_nue2.setText("");
                txt_con_nue2.requestFocus();
            }
        }
    }//GEN-LAST:event_txt_con_nue2KeyPressed

